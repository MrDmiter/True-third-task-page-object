package utils;

public class YamlFile {

    // Variables that reflect fields in the yaml file
    private String site;
    private String email;
    private String password;

    /**
     * Getters of the fields
     *
     * @return
     */
    public String getSite() {
        return site;
    }

    public String getEmail() {
        return email;
    }

    public String getPassword() {
        return password;
    }
}
